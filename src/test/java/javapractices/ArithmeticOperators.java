package javapractices;

public class ArithmeticOperators {

	public static void main(String[] args) {
		float a = 10, b = 20;
		System.out.println(a + b);//30
		System.out.println(a - b);//-10
		System.out.println(a * b);//200
		System.out.println(a / b);//0.5
		System.out.println(a % b);//10
	}

}
