package javapractices;

public class PositiveNegative {

	public static void main(String[] args) {

		int NumberA=34, NumberB=-12;

		// Checking for Positive Number

		if(NumberA>=0){
			System.out.println("No. is Positive : "+NumberA);	
		}
		else {
			System.out.println("No. is Negative : "+NumberA);	
		}

		System.out.println("##############################");

		// Checking for Negative Number

		if(NumberB>=0){
			System.out.println("No. is Positive : "+NumberB);	
		}
		else {
			System.out.println("No. is Negative : "+NumberB);	
		}
	}

}
